extract_by_polygon <- function(i, r, shape){
  
  # r <- text_tolima
  # shape <- tolima
  # i = 1
  
  values <- extract(r, shape[i, ]) %>%
    unlist()
  
  values <- data_frame(values, NOM_MUNICI = shape[i, ]@data$NOM_MUNICI)
  
  return(values)
}

